st.write("1. **Define (Definir):** Identifica o problema, define os objetivos do projeto e as necessidades dos clientes.")
st.write("2. **Measure (Medir):** Coleta dados relevantes para entender o processo atual e medir o desempenho.")
st.write("3. **Analyze (Analisar):** Examina os dados para identificar causas raiz dos problemas e áreas de melhoria.")
st.write("4. **Improve (Melhorar):** Desenvolve e implementa soluções para eliminar as causas raiz e melhorar o desempenho do processo.")
st.write("5. **Control (Controlar):** Estabelece controles e monitoramento para garantir que as melhorias sejam sustentáveis ao longo do tempo.")

